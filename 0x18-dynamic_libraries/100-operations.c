#include <stdio.h>

/**
 *add - adds int
 *@a: int a
 *@b: int b
 *Return: value of a + b
 */
int add(int a, int b)
{       
	return (a + b);
}       

/**
 *sub - subs int
 *@a: int a
 *@b: int b
 *Return: value of a - b
 */
int sub(int a, int b)
{       
	return (a - b);
}   

/**
 *mul - multiply ints
 *@a: int a
 *@b: int b
 *Return: value of a * b
 */
int mul(int a, int b)
{       
	return (a * b);
}

/**
 *div - divides int
 *@a: int a
 *@b: int b
 *Return: value of a / b
 */
int div(int a, int b)
{       
	return (a / b);
}   

/**
 *mod - find modulus
 *@a: int a
 *@b: int b
 *Return: value of a % b
 */
int mod(int a, int b)
{       
	return (a % b);
} 
